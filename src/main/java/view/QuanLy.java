    private void addDAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDAActionPerformed
        // TODO add your handling code here:
        String name = nameDA.getText();
        String type = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        String cost = costDA.getText();
        if (name == "" || type == "" || cost == "") {
            JOptionPane.showMessageDialog(null, "Nhap thieu thong tin");
            return;
        }
        try {
            int Cost = Integer.parseInt(cost);
            int currentPos = listDA.size() - 1;
            if (currentPos != -1) {
                DuAn.setCurrentId(listDA.get(currentPos).getId());
            }
            DuAn duan = new DuAn(Cost, name, type);
            for (int i = 0; i < listDA.size(); i++) {
                if (listDA.get(i).getName().equals(duan.getName())) {
                    JOptionPane.showMessageDialog(null, "trung ma du an");
                    return;
                }
            }
            listDA.add(duan);
            tableDA.addRow(duan.toObjects());
            loadID();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Gia nhap sai dinh dang");
        }
    }//GEN-LAST:event_addDAActionPerformed

