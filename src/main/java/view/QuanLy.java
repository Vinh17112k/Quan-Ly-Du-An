    private void loadData() {
        FileOP.readFromFile(listDA, File_DA);
        FileOP.readFromFile(listNV, File_NV);
        listDA.forEach((t) -> {
            tableDA.addRow(t.toObjects());
        });
        listNV.forEach((t) -> {
            tableNV.addRow(t.toObjects());
        });
    }

    private void loadID() {
        ID1.removeAllItems();
        loadID2.removeAllItems();
        listDA.forEach((t) -> {
            ID1.addItem(t.getId() + "");
        });
        listNV.forEach((t) -> {
            loadID2.addItem(t.getId() + "");
        });
    }

