    private void addNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNVActionPerformed
        // TODO add your handling code here:
        String name = nameNV.getText();
        String address = getAddress.getText();
        String phone = getPhone.getText();
        if (name == "" || address == "" || phone == "") {
            JOptionPane.showMessageDialog(null, "Nhap thieu thong tin");
            return;
        }
        int currentPos = listNV.size() - 1;
        if (currentPos != -1) {
            NhanVien.setCurrentId(listNV.get(currentPos).getId());
        }
        NhanVien nhanvien = new NhanVien(name, address, phone);
        for (int i = 0; i < listNV.size(); i++) {
            if (listNV.get(i).getName().equals(nhanvien.getName())) {
                JOptionPane.showMessageDialog(null, "trung nhan vien");
                return;
            }
        }
        listNV.add(nhanvien);
        tableNV.addRow(nhanvien.toObjects());
        loadID();
    }//GEN-LAST:event_addNVActionPerformed

