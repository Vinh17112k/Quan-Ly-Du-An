    private void addQuanLyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addQuanLyActionPerformed
        // TODO add your handling code here:
        int idQuanLy = ID1.getSelectedIndex();
        int idNhanVien = loadID2.getSelectedIndex();
        String ngay = getNgay.getText();
        try {
            int Ngay = Integer.parseInt(ngay);
            //QuanLy2 quanLly=new QuanLy2(DuAn, idNhanVien, Ngay);
            QuanLy2 q = new QuanLy2(listDA.get(idQuanLy), listNV.get(idNhanVien), Ngay);
            for (int i = 0; i < listQL.size(); i++) {
                if (listQL.get(i).getDuan().getName().equals(q.getDuan().getName()) && listQL.get(i).getNhanvien().getName().equals(q.getNhanvien().getName())) {
                    JOptionPane.showMessageDialog(null, "mot nhan vien khong the lam hai du an cung vi tri");
                    return;
                }
            }
            listQL.add(q);
            tableQL.addRow(q.toObjects());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "du lieu sai dinh dang");
        }
    }//GEN-LAST:event_addQuanLyActionPerformed

